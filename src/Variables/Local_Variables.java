package Variables;

public class Local_Variables {
public static void main(String [] args) {
	int i=20;
	System.out.println(i);
	
	long donky=65411237899l;
	System.out.println(donky); 
	
	char ch='A';
	System.out.println(ch);
	
	boolean b=true;
	System.out.println(b);
	
	String s="Velocity";
	System.out.println(s);
}
}
